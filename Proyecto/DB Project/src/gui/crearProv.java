    private void btnCrearProvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearProvMouseClicked
        // TODO add your handling code here:
        //Traemos los datos rellenados
        String idProv = txtIdProv.getText();
        String nombreprov = txtNombreProv.getText();
        int telProv = Integer.parseInt(txtTelefono.getText());
        System.out.println(telProv);
        String calleP = txtCalleProv.getText();
        String numP = txtNumeroProv.getText();
        String barrioP = txtBarrioProv.getText();
        String ciudadP = txtCiudadProv.getText();
        String compP = txtCompProv.getText();

        //REALIZAMOS INSCRIPCION DEL NUEVO EVENTO
        GestorEvento gestorprov = new GestorEvento();
        ProveedorDAO prov = gestorprov.getProveedorDAO();

        provDef = prov.getPr();

        if (idProv.isEmpty() || nombreprov.isEmpty() || txtTelefono.getText().isEmpty()
                || calleP.isEmpty() || numP.isEmpty() || numP.isEmpty() || barrioP.isEmpty()
                || ciudadP.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Uno de los dos campos está vacío", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } else {

            if (telProv <= 0) {
                JOptionPane.showMessageDialog(null, "El telefono es incorrecto", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            } else {
                provDef.setK_idp(Integer.parseInt(idProv));
                provDef.setN_nombrep(nombreprov);
                provDef.setO_telp(telProv);
                provDef.setN_callep(calleP);
                provDef.setN_nump(numP);
                provDef.setN_barriop(barrioP);
                provDef.setN_ciudadp(ciudadP);
                provDef.setN_compp(compP);
                provDef.setK_code(crearEvento.eventoDef.getK_code());

                prov.setPr(provDef);
                gestorprov.setProveedorDAO(prov);

                try {
                    gestorprov.incluirProveedor();
                    JOptionPane.showMessageDialog(null, "Inscripcion del proveedor correctamente", "", JOptionPane.INFORMATION_MESSAGE);
                } catch (CaException e) {
                    JOptionPane.showMessageDialog(null, "No se pudo realizar la inscripción", "ERROR", JOptionPane.WARNING_MESSAGE);
                }
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnCrearProvMouseClicked

