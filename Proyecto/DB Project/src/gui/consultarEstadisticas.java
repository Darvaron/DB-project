    private void btnGenerarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGenerarMouseClicked
        // TODO add your handling code here:
        String Filaob = null;
        DefaultTableModel modelo = (DefaultTableModel) TEstad.getModel();
        String idEv = txtIdEv.getText();
        GestorEvento consulta  = new GestorEvento();
        EventoDAO eventc = consulta.getEventoDAO();
        if (idEv.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo está vacío", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
        else {Evento even = eventc.getEv();
                even.setK_code(Integer.parseInt(idEv));
                eventc.setEv(even);
            try {
                Filaob = eventc.consultarEstadisticas();
            } catch (CaException ex) {
                Logger.getLogger(consultarEstadisticas.class.getName()).log(Level.SEVERE, null, ex);
            }
            String[] Filasp = Filaob.split(" ");
            modelo.addRow(Filasp);
                }
    }//GEN-LAST:event_btnGenerarMouseClicked

