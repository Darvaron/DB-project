    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            InscripcionDAO insDao = new InscripcionDAO();
            insReal = insDao.getIns();

            insReal.setK_code(Integer.parseInt(idEven.getText()));
            insDao.setIns(insReal);

            inscripciones = insDao.buscarInscripcion();
            for (int i = 0; i < modelo.getRowCount(); i++) {
                modelo.removeRow(i);
            }

            for (int i = 0; i < inscripciones.size(); i++) {
                String[] datos = {"" + inscripciones.get(i).getK_ins(), inscripciones.get(i).getF_inscrip(), inscripciones.get(i).getK_num() + "", inscripciones.get(i).getV_ins() + "", inscripciones.get(i).isO_asiste() + ""};
                modelo.addRow(datos);
            }

        } catch (CaException ex) {
            Logger.getLogger(ListarInscr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

