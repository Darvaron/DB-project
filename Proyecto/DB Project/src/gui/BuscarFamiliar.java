    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        try {
            DefaultTableModel modelo = (DefaultTableModel) tablaF.getModel();
            FamiliarDAO famDao = new FamiliarDAO();
            famiReal = famDao.getF();

            famiReal.setK_num(Integer.parseInt(idaso.getText()));
            famDao.setF(famiReal);

            familiares = famDao.buscarFamiliar();
            for (int i = 0; i < modelo.getRowCount(); i++) {
                modelo.removeRow(i);
            }

            for (int i = 0; i < familiares.size(); i++) {

                String[] datos = {familiares.get(i).getN_nombre1f(), familiares.get(i).getN_ap1f(), "" + familiares.get(i).getK_numidf(), familiares.get(i).getO_parentesco()};
                modelo.addRow(datos);
            }

        } catch (CaException ex) {
            Logger.getLogger(BuscarFamiliar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        try {
            FamiliarDAO famDao = new FamiliarDAO();
            famiReal = famDao.getF();

            famiReal.setK_numidf(Integer.parseInt(idfam.getText()));
            famiReal.setK_num(Integer.parseInt(idaso.getText()));
            famDao.setF(famiReal);

            famDao.eliminarFamiliar();
            JOptionPane.showMessageDialog(null, "Se ha eliminado el familiar");
        } catch (CaException ex) {
            Logger.getLogger(BuscarFamiliar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_borrarActionPerformed

